package bsu.rfe.java.group_8.lab_7.kononovich.var_b_3;


public class User {
    private String name;
    private String addres;

    public User (String name, String addres) {
        this.name = name;
        this.addres = addres;
    }
    public String getName() {
        return name;
    }

    public String getAddres(){
        return addres;
    }
}